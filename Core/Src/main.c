/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_I2C1_Init();
  MX_I2S3_Init();
  MX_FATFS_Init();
  MX_USB_HOST_Init();
  MX_I2C2_Init();
  MX_TIM2_Init();
  MX_ADC1_Init();
  MX_TIM3_Init();
  /* USER CODE BEGIN 2 */

  //Inicializacion interrupciones temporizador para receptor IR
  HAL_TIM_IC_Start_IT(&htim2, TIM_CHANNEL_2);

  //Inicializacion LCD
  lcd16x2_i2c_init(&hi2c2);
  lcd16x2_i2c_TwoLines();
  lcd16x2_i2c_setCursor(0, 5);
  lcd16x2_i2c_printf("Hola!!");
  lcd16x2_i2c_setCursor(0, 0);
  HAL_Delay(3000);

  //Inicializacion sensor de temperatura y su temporizador
  HAL_TIM_Base_Start(&htim3);
  HAL_ADC_Start_DMA(&hadc1, (uint32_t*)ADC_value, 3);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */
    MX_USB_HOST_Process();

    /* USER CODE BEGIN 3 */
    if(Appli_state==APPLICATION_READY)
    {
    	Mount_USB();
    	AUDIO_PLAYER_Start(0);
    	IsPlaying=1;
        while(!IsFinished)
        {
       		AUDIO_PLAYER_Process(true);
        	idx=getFilePos();
        	if(strcmp(nombre,(char*)FileList.file[idx].name))
        	{
        		strcpy(nombre,(char*)FileList.file[idx].name);
        		imprimeLCD(1, IsPlaying, nombre, temperatura);
        	}
        	if(flag_end)
        	{
        		decodeIR(&buffer,&device, &mensaje);
        		if(device==0xff)
        		{
        			switch(mensaje)
        			{
        			case 0xbf: //NEXT
        				AudioState=AUDIO_STATE_NEXT;
        				IsPlaying=1;
        				break;
        			case 0xbb: //PREV
        				AudioState=AUDIO_STATE_PREVIOUS;
        				IsPlaying=1;
        				break;
        			case 0xbc: //PLAY-PAUSE
        				if(AudioState==AUDIO_STATE_PLAY)
        				{
        					AudioState=AUDIO_STATE_PAUSE;
        					IsPlaying=0;
        				}
        				else if(AudioState==AUDIO_STATE_WAIT)
        				{
        					AudioState=AUDIO_STATE_RESUME;
        					IsPlaying=1;
        				}
        				break;
        			case 0xf8: //VOLUME DOWN
        				AudioState=AUDIO_STATE_VOLUME_DOWN;
        				break;
        			case 0xea: //VOLUME UP
        				AudioState=AUDIO_STATE_VOLUME_UP;
        				break;
        			}
        			flag_end=0;
        			imprimeLCD(1, IsPlaying, nombre, temperatura);
        		}
        	}
        	if(convCompleted)
        	{
        		temperatura=0.0f;
        		Vsense=0.0f;
        		for(int i=0;i<3;i++)
        		{
        			Vsense=((2910.0/0x3FF)*(float)ADC_value[i])/1000.0;
        			temperatura+=((Vsense-0.76)/0.0025)+25;
        		}
        		temperatura/=3;
        		imprimeLCD(1, IsPlaying, nombre, temperatura);
        		convCompleted=0;
        	}
       		if(AudioState==AUDIO_STATE_STOP)
      		{
      			IsFinished=1;
       		}
        }
    }
    else
    {
    	if(strcmp(nombre,"Insert USB"))
    	{
    		imprimeLCD(0, IsPlaying, nombre, temperatura);
    	}
    }
  }
  /* USER CODE END 3 */
}

